<?php
/**
 * Classe Turma
 */

namespace Model;

class Turma
{

    private $id;
    private $nome;

    public function __construct(int $id=0,$nome="")
    {
       $this->id = $id;
       $this->nome = $nome;
    }
    /**
     * Get the value of id
     */
    public function getId()
    {
        return $this->id;
    }

    /**
     * Set the value of id
     *
     * @return  self
     */
    public function setId($id)
    {
        $this->id = $id;

        return $this;
    }

    /**
     * Get the value of nome
     */
    public function getNome()
    {
        return $this->nome;
    }

    /**
     * Set the value of nome
     *
     * @return  self
     */
    public function setNome($nome)
    {
        $this->nome = $nome;

        return $this;
    }
}
